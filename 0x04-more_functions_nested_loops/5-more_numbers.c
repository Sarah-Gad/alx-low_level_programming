#include "main.h"
/**
 * more_numbers - a function that prints
 * 10 times the numbers, from 0 to 14,
 * followed by a new line.
*/

void more_numbers(void)
{
	int num_l;
	int nums;

	for (num_l = 0; num_l < 10; num_l++)
	{
		for (nums = 0; nums < 15; nums++)
		{
			;
		}
		putchar('\n');
	}
	putchar('\n');
}
