#include "3-calc.h"

/**
 * op_add - adds 2 nums
 * @a: the first num
 * @b: the second num
 * Return: the sum
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
 *op_sub - returns the difference
 *@a: first input
 *@b: second input
 *Return: the difference
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 *op_mul - returns the product
 *@a: first input
 *@b: second input
 *Return: the product
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 *op_div - returns result of the division
 *@a: first input
 *@b: second input
 *Return: the result of the division
*/
int op_div(int a, int b)
{
	return (a / b);
}


/**
 *op_mod - returns the  remainder
 *@a: first input
 *@b: second input
 *Return: the  remainder
*/
int op_mod(int a, int b)
{
	return (a % b);
}
